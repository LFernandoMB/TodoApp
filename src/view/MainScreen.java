    public void decorateTableTask(){
        tblTasks.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
        tblTasks.getTableHeader().setBackground(new Color(0,53,102));
        tblTasks.getTableHeader().setForeground(new Color(255, 255, 255));
        
        tblTasks.getColumnModel().getColumn(2).setCellRenderer(new DeadlineColumnCellRederer());
        
        tblTasks.getColumnModel().getColumn(4).setCellRenderer(new ButtonColumnCellRederer("edit"));
        
        tblTasks.getColumnModel().getColumn(5).setCellRenderer(new ButtonColumnCellRederer("delete"));
        
        tblTasks.setAutoCreateRowSorter(true); 
    }
    
    public void initDataController(){
        projectController = new ProjectController();
        taskController = new TaskController();
    }
    
    public void initComponentsModel(){
        projectsModel = new DefaultListModel();
        loadProjects();
        
        taskModel = new TaskTableModel();
        tblTasks.setModel(taskModel);
        
        if(!projectsModel.isEmpty()){
            lstProject.setSelectedIndex(0);
            Project project = (Project) projectsModel.get(0);
            loadTasks(project.getId());
        }
        
    }
    
    public void loadTasks(int idProject){
        List<Task> tasks = taskController.getAll(idProject);
        taskModel.setTasks(tasks);
        showTableTasks(!tasks.isEmpty());
    }
    
    private void showTableTasks(boolean hasTasks){
        if(hasTasks){
            if(pnlEmptyList.isVisible()){
                pnlEmptyList.setVisible(false);
                jPanel5.remove(pnlEmptyList);
            }
            jPanel5.add(srcPanelTasks);
            srcPanelTasks.setVisible(true);
            srcPanelTasks.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        } else {
            if (srcPanelTasks.isVisible()){
                srcPanelTasks.setVisible(false);
                jPanel5.remove(srcPanelTasks);
            }
            jPanel5.add(pnlEmptyList);
            pnlEmptyList.setVisible(true);
            pnlEmptyList.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        }
    }
    
